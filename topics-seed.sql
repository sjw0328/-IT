-- 2) 투표 시드 — 투표가 하나도 없는 토론에만, 접전(40~60%)으로 채움 ──
with d as (
  select id,
         0.40 + random() * 0.20            as bias,   -- 찬성 비율 40~60%
         (40 + floor(random() * 150))::int as nmax    -- 참여자 40~190명
  from debates
  where not exists (select 1 from votes v where v.debate_id = debates.id)
)
insert into votes (debate_id, user_id, side)
select d.id, u.id, case when random() < d.bias then 'A' else 'B' end
from d
join lateral (
  select id from users order by random() limit d.nmax
) u on true
on conflict (debate_id, user_id) do nothing;

